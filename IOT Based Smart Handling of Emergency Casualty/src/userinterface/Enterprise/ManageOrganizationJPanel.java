    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
    // Organization.Type type = (Organization.Type) organizationJComboBox.getSelectedItem();
      
     Type type = (Type) organizationJComboBox.getSelectedItem();
    
         if(type == Type.Doctor)
         {
          for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList())
          {
          if(organization.getName().equalsIgnoreCase("Doctor Organization"))
          {
              JOptionPane.showMessageDialog(null,"Doctor organization already exists for enterprise","Warning",JOptionPane.WARNING_MESSAGE);
          return;}
          }
              }
         else if(type == Type.Ambulance)
         {
         for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList())
          {
          if(organization.getName().equalsIgnoreCase("Ambulance Organization"))
          {  JOptionPane.showMessageDialog(null,"Ambulance organization already exists for enterprise","Warning",JOptionPane.WARNING_MESSAGE);
          return;}
          }
         }
         else if(type == Type.Receptionist)
         {
          for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList())
          {
          if(organization.getName().equalsIgnoreCase("Receptionist Organization"))
            
          {JOptionPane.showMessageDialog(null,"Receptionist organization already exists for enterprise","Warning",JOptionPane.WARNING_MESSAGE);
          return;}
          }
         }
         else{}
        enterprise.getOrganizationDirectory().createOrganization(type);
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed

