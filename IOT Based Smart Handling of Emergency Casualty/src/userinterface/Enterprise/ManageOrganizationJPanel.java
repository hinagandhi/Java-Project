    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        Enterprise.EnterpriseType type = enterprise.getEnterpriseType();
        if(type == Enterprise.EnterpriseType.EHS)
        {
        for (Type type1 : Organization.Type.values()){
        if (!type1.getValue().equals(Type.Admin.getValue())&&!type1.getValue().equals(Type.Doctor.getValue())
         &&!type1.getValue().equals(Type.Receptionist.getValue()))
                organizationJComboBox.addItem(type1);
        }
        }
        else
        {
        organizationJComboBox.addItem(Type.Doctor);
        organizationJComboBox.addItem(Type.Receptionist);
        }
    }
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
