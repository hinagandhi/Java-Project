    private void populateSigns()
    {
   HospitalWorkRequest request = (HospitalWorkRequest) workRequest;
//    System.out.println(request.getPerson().getName());
     //   EHSWorkRequest request = (EHSWorkRequest) workRequest;
    txtBP.setText(String.valueOf(request.getPerson().getVitalSign().getSystolicBP()));
    txtHeartRate.setText(String.valueOf(request.getPerson().getVitalSign().getHeartrate()));
    txtTemperature.setText(String.valueOf(request.getPerson().getVitalSign().getTemperature()));
    txtRespiRate.setText(String.valueOf(request.getPerson().getVitalSign().getRespiratoryrate()));
    DefaultTableModel model = (DefaultTableModel)tblList.getModel();
    model.setRowCount(0);
    for(String s :request.getInjuriesList())
    {
    Object[] row = new Object[1];
    row[0] = s;
    model.addRow(row);
    }
    }

