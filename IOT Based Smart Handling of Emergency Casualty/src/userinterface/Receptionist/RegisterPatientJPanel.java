    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        workRequest.setStatus("request accepted by hospital");
        Organization org = null; 
        for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList())
         {
         if(organization instanceof DoctorOrganization)
         {
         org = organization;
         break;
         }
         } 
       if(!org.getEmployeeDirectory().getEmployeeList().isEmpty())
       {
     HospitalWorkRequest hosRequest = (HospitalWorkRequest) workRequest;
     // EHSWorkRequest request = (EHSWorkRequest) workRequest;
      //request.setStatus(TOOL_TIP_TEXT_KEY);
      Date date = new Date();
  //    Person person = request.getPerson();
    //  hosRequest.setPerson(person);
      hosRequest.setStatus("Request received");
      hosRequest.setMessage("Please make preparations");
    //  hosRequest.setInjuriesList(request.getInjuriesList());
      hosRequest.setRequestDate(date);
      org.getWorkQueue().getWorkRequestList().add(hosRequest);
      JOptionPane.showMessageDialog(null,"The request has been assigned to doctor organization","Information",JOptionPane.INFORMATION_MESSAGE);
       btnAssign.setEnabled(false);
       }
       else
       {
       JOptionPane.showMessageDialog(null,"Doctor Organization is not ready to accept requests","warning",JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_btnAssignActionPerformed

